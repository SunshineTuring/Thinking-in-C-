#include <iostream>
using namespace std;

int main(){
    double i, j;
    cout << "Enter a double: " ;
    cin >> i;
    cout << "Enter another double: ";
    cin >> j;
    cout << "i > j is: " << i > j << endl; 
    cout << "i < j is: " << i < j << endl; 
    cout << "i >= j is: " << i >= j << endl; 
    cout << "i <= j is: " << i <= j << endl; 
    cout << "i == j is: " << i == j << endl; 
    cout << "i != j is: " << i != j << endl; 
    cout << "i && j is: " << i && j << endl; 
    cout << "i || j is: " << i || j << endl;     
}///:~
